export function generateStaticParams() {
  return Object.keys(starsData).map((slug) => ({ slug }));
}

/* -------------------------------------------------------------------------- */
/*  Metadata                                                                  */
/* -------------------------------------------------------------------------- */

export async function generateMetadata(props: {
  params: Promise<{ slug: string }>;
}): Promise<Metadata> {
  const { slug } = await props.params;
  const star = starsData[slug];
  if (!star) return { title: '找不到此星曜' };
  return {
    title: `${star.name}（${star.nickname}）`,
    description: star.coreDescription[0].slice(0, 120) + '...',
  };
}

